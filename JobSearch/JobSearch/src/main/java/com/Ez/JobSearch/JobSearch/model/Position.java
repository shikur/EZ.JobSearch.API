package com.Ez.JobSearch.JobSearch.model;

public class Position {
	
	private String id;
	private String title;
	private String EmployeType;
	private String companyType;
	private String companylocation ;
	private String startdateMMYYYY;
	private String enddateMMYYYY;
	private String headline;
	private String industry;
	private String description;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getEmployeType() {
		return EmployeType;
	}
	public void setEmployeType(String employeType) {
		EmployeType = employeType;
	}
	public String getCompanyType() {
		return companyType;
	}
	public void setCompanyType(String companyType) {
		this.companyType = companyType;
	}
	public String getCompanylocation() {
		return companylocation;
	}
	public void setCompanylocation(String companylocation) {
		this.companylocation = companylocation;
	}
	public String getStartdateMMYYYY() {
		return startdateMMYYYY;
	}
	public void setStartdateMMYYYY(String startdateMMYYYY) {
		this.startdateMMYYYY = startdateMMYYYY;
	}
	public String getEnddateMMYYYY() {
		return enddateMMYYYY;
	}
	public void setEnddateMMYYYY(String enddateMMYYYY) {
		this.enddateMMYYYY = enddateMMYYYY;
	}
	public String getHeadline() {
		return headline;
	}
	public void setHeadline(String headline) {
		this.headline = headline;
	}
	public String getIndustry() {
		return industry;
	}
	public void setIndustry(String industry) {
		this.industry = industry;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getMedia() {
		return media;
	}
	public void setMedia(String media) {
		this.media = media;
	}
	private String media;
	
	

}
