package com.Ez.JobSearch.JobSearch.model;

public class Education {
	
	private String school;
	private String degree;
	private String fieldOfStudy;
	private String startDateMMYYYY;
	private String enddateMMYYYY;
	
	private String grade;
	private String activities;
	private String descripton;
	private String socialMedia;
	public String getSchool() {
		return school;
	}
	public void setSchool(String school) {
		this.school = school;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getFieldOfStudy() {
		return fieldOfStudy;
	}
	public void setFieldOfStudy(String fieldOfStudy) {
		this.fieldOfStudy = fieldOfStudy;
	}
	public String getStartDateMMYYYY() {
		return startDateMMYYYY;
	}
	public void setStartDateMMYYYY(String startDateMMYYYY) {
		this.startDateMMYYYY = startDateMMYYYY;
	}
	public String getEnddateMMYYYY() {
		return enddateMMYYYY;
	}
	public void setEnddateMMYYYY(String enddateMMYYYY) {
		this.enddateMMYYYY = enddateMMYYYY;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getActivities() {
		return activities;
	}
	public void setActivities(String activities) {
		this.activities = activities;
	}
	public String getDescripton() {
		return descripton;
	}
	public void setDescripton(String descripton) {
		this.descripton = descripton;
	}
	public String getSocialMedia() {
		return socialMedia;
	}
	public void setSocialMedia(String socialMedia) {
		this.socialMedia = socialMedia;
	}
	
	

}
